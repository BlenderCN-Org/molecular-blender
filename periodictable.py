"""Collection of information for Periodic Table of Elements"""

class Element():
    """Element of the Periodic Table"""

    def __init__(self, vdw, cov, col, m, n, s):
        """Construct Element"""
        self.vdw = vdw
        self.covalent = cov
        self.color = col
        self.mass = m
        self.name = n
        self.symbol = s

    def __str__(self):
        """Return string of symbol"""
        return self.symbol


